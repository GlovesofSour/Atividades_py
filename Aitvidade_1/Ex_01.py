print('Digite o primeiro numero: ')
a = float(input())
print('Digite o segundo numero: ')
b = float(input())

print(f'A soma de {a} mais {b} é igual a: {a + b}')
print(f'A subtração de {a} menos {b} é igual a: {a - b}')
print(f'A multiplicação de {a} vezes {b} é igual a: {a * b}')
print(f'A divisão de {a} por {b} é igual a: {a / b}')